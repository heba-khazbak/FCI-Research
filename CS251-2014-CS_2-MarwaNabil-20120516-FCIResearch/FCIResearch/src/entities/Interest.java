package entities;

public class Interest {

	private String name;

	private String type;

  public void setName(String data){
	  name=data;
  }
  
  public void setType(String data){
	  type=data;
  }
  
  public String getName(){
	  return name;
  }
  
  public String getType(){
	  return type;
  }
  

}